    public String[] datos(String facultad) {
        String[] programas = new String[3];
        switch (facultad) {
            case "Ingeniería":
                programas[0] = "Ingenieria InDustrial";
                programas[1] = "Ingenieria Sistemas";
                programas[2] = "Ingenieria Software";
                break;
            case "Ciencias Empresariales":
                programas[0] = "Administración Logísticas";
                programas[1] = "Administración de Empresas ";
                programas[2] = "Administración de servicios de salud";
                break;
            case "Contaduría y Finanzas Empresariales":
                programas[0] = "Finanzas y negocios Intenacionales";
                programas[1] = "Contaduría Pública ";
                programas[2] = "Tecnología en Auditoría y aseguramiento de la información";
                break;
            case "Comunicación":
                programas[0] = "Mercadeo y Publicidad";
                programas[1] = "Diseño Visual";
                programas[2] = "Comunicacion Social";
                break;
            default:
                JOptionPane.showMessageDialog(rootPane, "Opción inválida");
        }
        return programas;
    }
    public void mostrar(){
        String datos[][] = new String [lista.size()][5];
        for(int i=0; i<lista.size(); i++){
            datos[i][0]=lista.get(i).getNombre();
            //lo tengo que convertir a string para que se guarde correctamente en el array datos
            datos[i][1]=String.valueOf(lista.get(i).getCedula());
            datos[i][2]=String.valueOf(lista.get(i).getEdad());
            datos[i][3]=lista.get(i).getFacultad();
            datos[i][4]=lista.get(i).getPrograma();
        }
        //le asignamos mediante la funcion set model a la tabla Datos, los datos de docente y ademas
        //le asigna los titulos a las columnas 
        tablaDatos.setModel(new javax.swing.table.DefaultTableModel (datos,
                        new String[]{"Nombre", "Cedula","Edad","Facultad","Programa"}));
    }
    public void limpiar(){
        txtNombre.setText("");
        txtCedula.setText("");
        txtEdad.setText("");
        cmbFacultad.setSelectedIndex(0);
        cmbPrograma.setSelectedItem("");
    }
    
