    private void cmbFacultadItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbFacultadItemStateChanged
        //verifico si se selecciona un item
        if (evt.getStateChange()==ItemEvent.SELECTED){
            //verificamos si ya selecciono una facultad
            if(this.cmbFacultad.getSelectedIndex()>0){
                //asignamos el string que escogio a combo programa
                this.cmbPrograma.setModel(new DefaultComboBoxModel(
                        //mamamos funcion datos le pasamos por argumento la string de facultad
                        this.datos(this.cmbFacultad.getSelectedItem().toString())));
            }
        }
    }//GEN-LAST:event_cmbFacultadItemStateChanged

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try{
            int cod= Integer.parseInt(txtCedula.getText());
            int ed= Integer.parseInt(txtEdad.getText());
            Docentes docente = new Docentes (txtNombre.getText(),cod,ed,
                        (String) cmbFacultad.getSelectedItem(),
                        (String) cmbPrograma.getSelectedItem());
            lista.add(docente);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
        //este metodo envia los datos a la tabla
        mostrar();
        //este metodo limpia las casillas del formulario
        limpiar();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnCargarDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarDatosActionPerformed
        //Nos retorna la fila que estamos seleccionando
        //tomo el valor de la fila seleccionada 
        filaseleccionada=tablaDatos.getSelectedRow();
        if(filaseleccionada != -1){
            //si se selecciona una fila entonces
            //campos de texto, con la funcion getvalue solicitamos la fila y la columna
            txtNombre.setText(tablaDatos.getValueAt(filaseleccionada, 0).toString());
            txtCedula.setText(tablaDatos.getValueAt(filaseleccionada, 1).toString());
            txtEdad.setText(tablaDatos.getValueAt(filaseleccionada, 2).toString());
            cmbFacultad.getModel().setSelectedItem(tablaDatos.getValueAt(filaseleccionada, 3));
            cmbPrograma.getModel().setSelectedItem(tablaDatos.getValueAt(filaseleccionada, 4));
        } else {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar una fila");
        }
    }//GEN-LAST:event_btnCargarDatosActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // ese modifica las filas de la tabla
        //no tomo el valor de las filas seleccionadas porque ya esta seleccionada
        if(filaseleccionada != -1){
            tablaDatos.setValueAt(txtNombre.getText(), filaseleccionada, 0);
            tablaDatos.setValueAt(txtCedula.getText(), filaseleccionada, 1);
            tablaDatos.setValueAt(txtEdad.getText(), filaseleccionada, 2);
            tablaDatos.setValueAt(cmbFacultad.getSelectedItem(), filaseleccionada, 3);
            tablaDatos.setValueAt(cmbPrograma.getSelectedItem(), filaseleccionada, 4);
            filaseleccionada = -1;
        } else {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar una fila");
        }
        limpiar();

    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //Asignamos la fila que selecciona el usuario a fila seleccionada
        filaseleccionada=tablaDatos.getSelectedRow();
        //cremos una variable para asignarle el id de los datos
        //del docente a eliminar
        int cedula;
        //damos a cedula el valor de la cedula de la fila que selecciono el usuario
        cedula=(int)(tablaDatos.getValueAt(filaseleccionada,1));
        //con un for recogemos los datos en la lista
        for(int i=0; i<lista.size();i++){
           if(cedula==lista.get(i).getCedula()){
               //eliminados los datos del docente seleccionado
               lista.remove(i);
           } 
        }
        mostrar();//actualizamos los datos
    }//GEN-LAST:event_btnEliminarActionPerformed

